function checkIfExists($table_name, $search_row, $matches){
    $ci =& get_instance();
    $ci->load->database();
    $query = $ci->db->where($search_row, $matches)->get($table_name);
    $rowCount = $query->num_rows();
    if($rowCount == 1){
        return true;
    }else{
        return false;
    }
}

