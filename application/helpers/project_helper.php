function limitChars($string, $limit, $delimiter="..."){
    if(strlen($string) < $limit){
        $returnedString = mb_substr($string, 0, $limit);
    }else{
        $returnedString = mb_substr($string, 0, $limit) . $delimiter;
    }
    return $returnedString;

}

