function dateTranslate($string){
    $monthsEng = array(
        'Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec', 'Sun', 'Mon', 'Thu', 'Wed', 'Tue', 'Fri', 'Sat');
    $monthsTr = array(
        'Ocak','Şubat','Mart','Nisan','Mayıs','Haziran','Temmuz','Ağustos','Eylül','Ekim','Kasım','Aralık', 'Pazar', 'Pazartesi', 'Salı', 'Çarşamba', 'Perşembe', 'Cuma', 'Cumartesi');
    return str_replace($monthsEng, $monthsTr, $string);
}

